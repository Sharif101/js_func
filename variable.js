// var variable example
var a = "Hi var this is first";
console.log(a);
a = "Hi var now i am modify";
console.log(a); // u can modify or change value if u declare it var or let

// let variable example
let b = "Hi let this is first";
console.log(b);
b = "Hi let now i am modify";
console.log(b);

//const variable example
const c = "Hi const this is first";
console.log(c);
c = "Hi const now i am modify";
console.log(c); // u can't modify or change if u declare it already const variable
